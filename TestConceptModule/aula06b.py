n = input('Digite um valor: ')
print(type(n)) # printa o tipo do valor
print(n.isnumeric()) # metodo is numeric vê se o valor é possivel converter para numérico no input
print(n.isalpha()) # metodo is alpha vê se o valor é letra
print(n.isalnum()) # metodo is alpha numério (3a) vê se o valor é letra


